public class bitwiseopertors {
    public static void main(String[] args) {
        int p=9;
        int q=10;
        System.out.println(p|q);
        System.out.println(p&q);
        System.out.println(p^q);
        System.out.println(p<<1);
        System.out.println(p<<2);
        System.out.println(q>>1);
        System.out.println(q>>2);
        System.out.println(~p);
    }
}